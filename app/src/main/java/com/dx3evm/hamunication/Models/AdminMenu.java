package com.dx3evm.hamunication.Models;

public class AdminMenu {
    private String title;

    private int img;

    public AdminMenu() {}

    public AdminMenu(int img, String title) {
        this.img = img;
        this.title = title;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
